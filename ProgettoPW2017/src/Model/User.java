package Model;

/**
 *	Gestione dell'utente e di tutte le sue informazioni
*/
public class User
{
	private String username;
	private String nome;
	private String cognome;
	private String password;
	private int Amministratore;
	private String indirizzo;
	private Carta carta;
	
	public User()
	{}
	
	/**Prende l'username di una determinata istanza d'user
	 * @return Valore dell'username
	 */	
	public String getUsername()
	{
		return username;
	}

	/**Definisce l'username di un utente
	 * @param username
	 */
	public void setUsername(String username)
	{
		this.username = username;
	}
	/**Prende il nome di un utente*/
	public String getNome()
	{
		return nome;
	}
	
	/**Definisce il nome di un utente
	 * @param nome Nome dell'utente
	 */
	public void setNome(String nome)
	{
		this.nome = nome;
	}

	public String getCognome()
	{
		return cognome;
	}

	public void setCognome(String cognome)
	{
		this.cognome = cognome;
	}

	public String getPassword()
	{
		return password;
	}

	public void setPassword(String password)
	{
		this.password = password;
	}

	public int getAmministratore()
	{
		return Amministratore;
	}

	public void setAmministratore(int amministratore)
	{
		Amministratore = amministratore;
	}

	public String getIndirizzo()
	{
		return indirizzo;
	}

	public void setIndirizzo(String indirizzoStandard)
	{
		this.indirizzo = indirizzoStandard;
	}
	
	public Carta getCarta()
	{
		return carta;
	}
	
	/** A differenza dei normali metodi get di questa classe questo elemento aggiunge
	 * una carta ad una lista di carte che il cliente possiede
	 * @param x Carta da aggiungere alla liste di carte che il cliente possiede
	 */
	public void setCarta(Carta x)
	{
		this.carta = x;
	}
}
